"""problema_13
convertir temperatura de Celsius a Fahrenheit"""

temp_c1 = 30
temp_c2 = 22

temp_f1 = (temp_c1 * 9) / 5 + 32
temp_f2 = (temp_c2 * 9) / 5 + 32

print(f"La temperatura en °C: {temp_c1}")
print(f"Temperatura en Fahrenheit: {temp_f1:.2f}")

print(f"\nLa temperatura en °C: {temp_c2}")
print(f"Temperatura en Fahrenheit: {temp_f2:.2f}")